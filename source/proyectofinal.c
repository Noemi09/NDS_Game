void ConfigurarInterrupciones()
{
    //inicializamos temporizadores
    irqEnable(IRQ_TIMER0);
    irqSet(IRQ_TIMER0, timer_personas); //timer para las personas que se mueven por la acera
    TIMER_DATA(0) = 32764;
    TIMER_CR(0) = TIMER_DIV_1024 | TIMER_ENABLE | TIMER_IRQ_REQ;

    irqEnable(IRQ_TIMER1);
    irqSet(IRQ_TIMER1, timer_necesidades); //timer para que las columnas de las necesidades vayan bajando
    TIMER_DATA(1) = 0;  //32764 ----> para que vaya muy rapido
    TIMER_CR(1) = TIMER_DIV_1024 | TIMER_ENABLE | TIMER_IRQ_REQ;   //1 ----> para que vaya muy rapido

    irqEnable(IRQ_TIMER2);
    irqSet(IRQ_TIMER2, timer3); //animación IDLE
    TIMER_DATA(2) = 32764;
    TIMER_CR(2) = TIMER_DIV_1024 | TIMER_ENABLE | TIMER_IRQ_REQ;

    irqSet(IRQ_TIMER3, timer4); //animación IDLE
    TIMER_DATA(3) = 0;
    TIMER_CR(3) = TIMER_DIV_64 | TIMER_ENABLE | TIMER_IRQ_REQ;
}

void botones_habilitados()
{
    u16 keys_curr = ~REG_KEYINPUT; // Lee el estado actual de los botones (invierte los bits porque los botones se activan con 0)

    if (gameMode == 1)
    {
       // Si se pulsa el botón A y estamos en la opción 1 (por ejemplo)
        if ((keys_curr & KEY_A) && (opcion == 1))
        {
            InicializarJuego();
        }

        // Si se pulsa la flecha izquierda o derecha
        if (keys_curr & (KEY_LEFT | KEY_RIGHT))
        {
            MoverSelector();
        }
    }

    else if (gameMode == 2)
    {
        // Si se pulsa una de las teclas de dirección
        if (keys_curr & (KEY_UP | KEY_DOWN | KEY_LEFT | KEY_RIGHT | KEY_A))
        {
            MoverSim();
        }
    }

    else if (gameMode == 3)
    {
        // Si se pulsa el botón Start
        if (keys_curr & KEY_START)
        {
            PantallaInicio();
        }
    }
}

